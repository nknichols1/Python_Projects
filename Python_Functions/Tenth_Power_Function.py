# Write a function named tenth_power() that has one parameter named num.
#The function should return num raised to the 10th power.


def tenth_power(num):
  return num ** 10


print(tenth_power(1))
# 1 to the 10th power is 1
print(tenth_power(0))
# 0 to the 10th power is 0
print(tenth_power(2))
# 2 to the 10th power is 1024
